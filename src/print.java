public abstract class print {
    public static void ln(int x) {
        System.out.println(x);
    }
    public static void ln(double x) {
        System.out.println(x);
    }
    public static void ln(String x) {
        System.out.println(x);
    }
    public static void ln(char x) {
        System.out.println(x);
    }
}
